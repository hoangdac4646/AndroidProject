public class GuiTinNhan {
    private String TinNhan,TenThietBi;


    public GuiTinNhan(String tinNhan, String tenThietBi) {
        TinNhan = tinNhan;
        TenThietBi = tenThietBi;
    }

    public String getTinNhan() {
        return TinNhan;
    }

    public String getTenThietBi() {
        return TenThietBi;
    }
}
